package com.shoppingcart.checkout;

/**
 * @author ${dheeraj.kotha}
 */
public class Products {

    private String offer;

    private double apples;

    private double oranges;

    private double bananas;

    public String getOffer() {
        return offer;
    }

    public void setOffer(String offer) {
        this.offer = offer;
    }

    public double getApples() {
        return apples;
    }

    public void setApples(double apples) {
        this.apples = apples;
    }

    public double getOranges() {
        return oranges;
    }

    public void setOranges(double oranges) {
        this.oranges = oranges;
    }

    public double getBananas() {
        return bananas;
    }

    public void setBananas(double bananas) {
        this.bananas = bananas;
    }
}
